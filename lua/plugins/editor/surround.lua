-- nvim-surround 包围符号操作（添加/修改/删除括号、引号、标签等包围符号）
--
-- =============================================================================
-- 基本操作（Normal 模式）
-- =============================================================================
--
-- ┌──────────────────────┬─────────────────────────────────────────────────┐
-- │ 快捷键               │ 说明                                            │
-- ├──────────────────────┼─────────────────────────────────────────────────┤
-- │ ys{motion}{char}     │ 添加包围符（you surround）                      │
-- │ ds{char}             │ 删除包围符（delete surround）                   │
-- │ cs{old}{new}         │ 替换包围符（change surround）                   │
-- └──────────────────────┴─────────────────────────────────────────────────┘
--
-- =============================================================================
-- 一、添加包围符：ys{motion}{char}
-- =============================================================================
--
-- 示例（光标在单词 hello 上）：
--   ysiw"    →  "hello"         用双引号包围当前单词
--   ysiw'    →  'hello'         用单引号包围当前单词
--   ysiw(    →  ( hello )       用括号包围（带空格）
--   ysiw)    →  (hello)         用括号包围（不带空格）
--   ysiw[    →  [ hello ]       用方括号包围（带空格）
--   ysiw]    →  [hello]         用方括号包围（不带空格）
--   ysiw{    →  { hello }       用花括号包围（带空格）
--   ysiw}    →  {hello}         用花括号包围（不带空格）
--   ysiw`    →  `hello`         用反引号包围
--   ysiw*    →  *hello*         用星号包围
--
-- 行使操作（"ss" 表示整行操作，方便记忆）：
--   yss"     →  整行用双引号包围
--   yss(     →  整行用括号包围（带空格）
--   yss)     →  整行用括号包围（不带空格）
--   yssb     →  整行用括号包围（同 yss)）
--   yssB     →  整行用花括号包围（同 yss}）
--
-- 使用 motion 选择范围：
--   yse"     →  从光标到单词结尾用双引号包围
--   ys$"     →  从光标到行尾用双引号包围
--   ysf,"    →  从光标到下一个逗号用双引号包围
--   ys2w"    →  从光标起 2 个单词用双引号包围
--
-- 视觉模式（选中文本后按 S + 目标字符）：
--   S"       →  选中文本用双引号包围
--   S(       →  选中文本用括号包围（带空格）
--   St<div>  →  选中文本用 <div> 标签包围
--
-- =============================================================================
-- 二、删除包围符：ds{char}
-- =============================================================================
--
-- 示例：
--   ds"      →  删除最近的成对双引号 "hello" → hello
--   ds'      →  删除最近的成对单引号
--   ds(       →  删除最近的成对括号
--   ds[       →  删除最近的成对方括号
--   ds{       →  删除最近的成对花括号
--   dst       →  删除最近的成对标签 <div>hello</div> → hello
--   ds`       →  删除最近的成对反引号
--   ds*       →  删除最近的成对星号
--
-- =============================================================================
-- 三、替换包围符：cs{old}{new}
-- =============================================================================
--
-- 示例：
--   cs"'      →  "hello" → 'hello'       双引号换成单引号
--   cs"(      →  "hello" → ( hello )      双引号换成括号（带空格）
--   cs")      →  "hello" → (hello)        双引号换成括号（不带空格）
--   cs'"      →  'hello' → "hello"        单引号换成双引号
--   cs(<div>  →  (hello) → <div>hello</div> 括号换成 div 标签
--   cst"      →  <div>hello</div> → "hello" 标签换成双引号
--   cs]'      →  [hello] → 'hello'        方括号换成单引号
--   cs}`      →  {hello} → `hello`        花括号换成反引号
--
-- =============================================================================
-- 四、行首/行尾快捷方式
-- =============================================================================
--
--   yss 系列专用于整行操作（见上方），无需 motion。
--
-- =============================================================================
-- 五、HTML/XML 标签操作
-- =============================================================================
--
--   ysiwt<tag>  给当前单词添加标签（如 ysiwt<div>）
--   yss<t>      给整行添加 <t> 标签
--   cst<tag>    将当前包围符替换为 <tag> 标签
--   dst         删除最近的标签对
--
-- 在标签名后按 Enter 键完成输入，或按 > 自动添加 > 闭合。
--
-- 视觉模式：
--   S<tag>      选中文本用标签包围（如选中文本后 S<div>）
--   S<h1        选中文本用 <h1> 标签包围（自动补全 >）
--
-- =============================================================================
-- 六、自定义别名（可以通过 setup 配置）
-- =============================================================================
--
-- 常用字母<div>对应</div>的包围符：
--   ) 或 b  →  括号 (无空格)
--   ( 或 B  →  括号 (有空格)
--   ] 或 r  →  方括号 (无空格)
--   [ 或 R  →  方括号 (有空格)
--   } 或 B  →  花括号 (无空格)
--   { 或 R  →  花括号 (有空格)
--   "       →  双引号
--   '       →  单引号
--   `       →  反引号
--   *       →  星号
--   t       →  标签
--   f       →  函数调用（如 fname(args)）
--   s       →  空格（如 hello world → { hello world }）
--
-- 注：带空格/不带空格的区别适用于括号类字符。
--   开括号（如 '('）表示添加空格，闭括号（如 ')'）表示不添加空格。
--
-- =============================================================================
-- 七、移动光标到开始/结束标签（. 重复操作支持）
-- =============================================================================
--
-- 使用 . 可以重复上一次的包围操作，非常高效。

return {
  "kylechui/nvim-surround",
  version = "*",
  event = { "BufReadPre", "BufNewFile" },
  config = function()
    require("nvim-surround").setup({})
  end,
}
